#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// Функция для подсчета количества корректных UTF-8 символов в строке
int count_utf8_symbols(const char *str) {
    int count = 0;
    while (*str) {
        // В UTF-8 байты многобайтного символа (кроме первого) имеют вид 10xxxxxx (от 0x80 до 0xBF)
        // Мы считаем только те байты, которые НЕ являются продолжением (то есть начальные байты символов)
        if ((*str & 0xC0) != 0x80) {
            count++;
        }
        str++;
    }
    return count;
}

int main() {
    // Настраиваем консоль для корректного вывода UTF-8 на случай Windows/Linux
    #ifdef _WIN32
    system("chcp 65001 > nul");
    #endif

    char text[500];

    printf("====================================================\n");
    printf(" Лабораторна робота No 7. Задача 7.1\n");
    printf(" Виконав: Султан Аль-Газали (Заочна форма)\n");
    printf(" Тема: Обробка символьної інформації за стандартом Unicode\n");
    printf("====================================================\n\n");

    printf("Введіть рядок тексту (українською або англійською): ");

    // Безопасный ввод строки с пробелами
    if (fgets(text, sizeof(text), stdin) != NULL) {
        // Удаляем символ переноса строки \n, если он есть
        text[strcspn(text, "\n")] = '\0';

        int bytes_len = strlen(text);
        int symbols_len = count_utf8_symbols(text);

        printf("\n--- Результати аналізу рядка ---\n");
        printf("Введений текст: \"%s\"\n", text);
        printf("Кількість зайнятих байтів у пам'яті: %d\n", bytes_len);
        printf("Фактична кількість символів (Unicode): %d\n", symbols_len);

        if (bytes_len > symbols_len) {
            printf("Аналіз: Рядок містить багатобайтні символи кирилиці (UTF-8).\n");
        } else {
            printf("Аналіз: Рядок містить лише однобайтні символы ASCII (латиниця/цифри).\n");
        }
    } else {
        printf("Помилка введення даних!\n");
    }

    printf("\n====================================================\n");
    return 0;
}
