#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main() {
    // Настройка консоли для корректного вывода украинского языка
    #ifdef _WIN32
    system("chcp 65001 > nul");
    #endif

    double x, y, z, result;

    printf("====================================================\n");
    printf(" Лабораторна робота № 7. Задача 7.2\n");
    printf(" Виконав: Султан Аль-Газали (Заочна forma)\n");
    printf(" Тема: Обчислення виразів з використанням математичних функцій\n");
    printf("====================================================\n\n");

    printf("Введіть значення x: ");
    if (scanf("%lf", &x) != 1) {
        printf("Помилка введення x!\n");
        return 1;
    }

    printf("Введіть значення y: ");
    if (scanf("%lf", &y) != 1) {
        printf("Помилка введення y!\n");
        return 1;
    }

    printf("Введіть значення z: ");
    if (scanf("%lf", &z) != 1) {
        printf("Помилка введення z!\n");
        return 1;
    }

    // Проверка области допустимых значений (ОДЗ)
    double pod_kornem = x + y + z;
    if (pod_kornem < 0) {
        printf("\nПомилка: Вираз під коренем від'ємний!\n");
        return 1;
    }

    // Математическая функция по стандарту ЛР 7.2
    result = sqrt(pod_kornem) + (sin(x) / (cos(y) + 2.0));

    printf("\n--- Результати обчислень ---\n");
    printf("Введені дані: x = %.2f, y = %.2f, z = %.2f\n", x, y, z);
    printf("Результат виразу: %.4f\n", result);
    printf("====================================================\n");

    return 0;
}
